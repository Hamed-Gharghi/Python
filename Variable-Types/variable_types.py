# Description: Demonstrates the usage of basic variable types in Python.
# It includes examples of integers, floats, strings, and booleans.

# Integer Variable
age = 25
print("Integer Example:")
print("Age:", age)  # Output the value of the integer variable
print("Type of age:", type(age))  # Output the type of the variable
print()  # Adding a blank line for better readability

# Float Variable
height = 5.9
print("Float Example:")
print("Height:", height)  # Output the value of the float variable
print("Type of height:", type(height))  # Output the type of the variable
print()  # Adding a blank line for better readability

# String Variable
name = "Alice"
greeting = "Hello, " + name + "!"  # Concatenate strings to create a greeting
print("String Example:")
print("Greeting:", greeting)  # Output the concatenated string
print("Type of greeting:", type(greeting))  # Output the type of the variable
print()  # Adding a blank line for better readability

# Boolean Variable
is_student = True
is_employed = False
print("Boolean Example:")
print("Is student:", is_student)  # Output the value of the boolean variable
print("Is employed:", is_employed)  # Output another boolean variable
print("Type of is_student:", type(is_student))  # Output the type of the variable
print()  # Adding a blank line for better readability
